//Loops allow us to run the same code multiple times untill a conditon is fufilled//

//Iterate with While loops//
var myArray = [];

var i = 0;
while (i < 5) {
  myArray.push(i);
  i++;
}
console.log(myArray);

//Iterate with For loops//
var ourArray = [];

for (var i = 0; i < 5; i++) {
  ourArray.push(i);
}
console.log(ourArray);

